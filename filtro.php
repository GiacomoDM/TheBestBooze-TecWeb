function filtroRic($input){
        $arr = array('!' => '' , "\"" => '' , '#' => '' , '$' => '' , '%' => '' , '&' => '' , '(' => '' , ')' => '' ,
                '*' => '' , '+' => '' , ':' => '' , ';' => '' , '<' => '',
